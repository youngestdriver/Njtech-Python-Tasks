'''
年底了，某公司要发年终奖。列表lst_staff中存放了所有员工的名单，
内容为["李梅","张富","付妍","赵诺","刘江"]。
字典dic_award中存放了对公司有杰出贡献的员工名单及奖金金额，
内容为{"张富":10000,"赵诺":15000}。
试编写程序，输出指定员工的年终奖金额。
注：
1、不在字典中的员工按照5000元/人发放年终奖。
2、不在员工列表中的人员输出“该员工不存在”
样例1：
输入：
张富
输出
10000
样例2：
输入：
张三
输出：
该员工不存在
样例3：
输入：
李梅
输出
5000
'''
# 员工名单和奖金字典
lst_staff = ["李梅", "张富", "付妍", "赵诺", "刘江"]
dic_award = {"张富": 10000, "赵诺": 15000}

# 获取输入
employee_name = input()

# 检查员工是否存在
if employee_name not in lst_staff:
    print("该员工不存在")
else:
    # 检查员工是否在奖金字典中
    if employee_name in dic_award:
        print(dic_award[employee_name])
    else:
        print(5000)
